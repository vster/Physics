% Rules for the "bra-ket" formalism
% 1.
% |Psi1>=OpA|phi1>,  <Psi2|=<phi2|OpB              (3.1.5)
% 2.
% <u|...|v>  - scalar
% |u>...<v|  - operator                            (3.1.6)
% 3.
% ...|u>  - ket-vectors
% <v|...  - bra-vectors
% 4. 
% We can swap bra- and ket forms
% vectors       |phi> <-> <phi|
% scalars (numbers) a <-> a' (Complex conjugation)        (3.1.7)
% operators       OpA <-> OpA' (Hermitian conjugation)
% <v|u> = <u|v>'
% OpA|phi>=lam|phi>  <=> <phi|OpA'=lam'*<phi|
% If OpA is Hermitian then <phi|OpA=lam*<phi|
% (OpA*OpB)'=OpB'*OpA'
% OpP=|u><v| => OpP'=(|u><v|)'=|v><u|                     (3.1.8)
